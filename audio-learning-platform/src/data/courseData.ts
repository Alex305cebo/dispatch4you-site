// Группировка уроков по категориям
export const lessonsByCategory = lessons.reduce((acc, lesson) => {
    if (!acc[lesson.category]) {
        acc[lesson.category] = [];
    }
    acc[lesson.category].push(lesson);
    return acc;
}, {} as Record<string, Lesson[]>);

// Получение всех категорий
export const categories = Array.from(new Set(lessons.map(l => l.category)));

// Утилиты для работы с данными
export const getLessonBySlug = (slug: string): Lesson | undefined => {
    return lessons.find(lesson => lesson.slug === slug);
};

export const getLessonsByCategory = (category: string): Lesson[] => {
    return lessons.filter(lesson => lesson.category === category);
};

export const getTotalDuration = (): string => {
    const totalSeconds = lessons.reduce((acc, lesson) => {
        const [min, sec] = lesson.duration.split(':').map(Number);
        return acc + min * 60 + sec;
    }, 0);

    const hours = Math.floor(totalSeconds / 3600);
    const minutes = Math.floor((totalSeconds % 3600) / 60);

    return hours > 0 ? `${hours}ч ${minutes}мин` : `${minutes}мин`;
};
