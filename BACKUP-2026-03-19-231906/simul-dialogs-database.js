// Функции для работы с базой данных
function getRandomDialogs(count = 5) {
    const shuffled = [...simulDialogsDatabase].sort(() => 0.5 - Math.random());
    return shuffled.slice(0, count);
}

function getDialogById(id) {
    return simulDialogsDatabase.find(d => d.id === id);
}

function getDialogsByBrokerStyle(style) {
    return simulDialogsDatabase.filter(d => d.brokerStyle === style);
}

function getDialogsByEquipment(equipment) {
    return simulDialogsDatabase.filter(d => d.equipment === equipment);
}

function getDialogsByDifficulty(difficulty) {
    return simulDialogsDatabase.filter(d => d.difficulty === difficulty);
}

